from hashlib import sha256
import re

digits58 = '123456789ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz'


def decode_base58(bc, length):
    n = 0
    for char in bc:
        n = n * 58 + digits58.index(char)
    return n.to_bytes(length, 'big')


def check_bc(bc):
    try:
        bcbytes = decode_base58(bc, 25)
        return bcbytes[-4:] == sha256(sha256(bcbytes[:-4]).digest()).digest()[:4]
    except Exception:
        return False


def find_all_regex_ocurrences_btc(text):
    return re.findall("[13][a-km-zA-HJ-NP-Z1-9]{25,34}", text)


def get_all_bitcoin_address_from_text(text):
    addresses = set(find_all_regex_ocurrences_btc(text))
    wallets = [a for a in addresses if check_bc(a)]
    print('Address', len(wallets), ' found!')
    return wallets


